# -*- coding: utf-8 -*-
"""
config/settings.py

프로그램 전역에서 사용하는 상수 및 설정값을 모아두는 모듈입니다.
정규식 패턴, 난이도 키워드, 파일 경로 등을 이곳에서 관리하면
추후 규칙이 바뀌어도 이 파일만 수정하면 되므로 유지보수가 쉬워집니다.
"""

import os

# -----------------------------
# 경로 관련 설정
# -----------------------------
# 결과 txt 파일이 저장될 기본 출력 폴더
OUTPUT_DIR = os.path.join(os.getcwd(), "output")

# 예외/에러 로그가 기록될 파일 경로
ERROR_LOG_PATH = os.path.join(os.getcwd(), "error_log.txt")

# 난이도별 결과 HWP 파일이 저장될 폴더 (OUTPUT_DIR 하위)
OUTPUT_HWP_DIR = OUTPUT_DIR

# 기존 결과 HWP 파일을 수정하기 전, 자동 백업이 저장될 폴더
BACKUP_DIR = os.path.join(os.getcwd(), "backup")

# 예외 파일 재처리 시, 성공한 문제가 저장될 폴더 (output/정상/재처리완료/)
REPROCESS_OUTPUT_DIR = os.path.join(OUTPUT_DIR, "정상", "재처리완료")

# 예외 문제 저장 파일 경로 (난이도/중단원 오류 문제)
# ExceptionWriter가 이 폴더 아래에 exceptions.json / exceptions.txt 를 생성합니다.
EXCEPTION_OUTPUT_DIR = OUTPUT_DIR


# -----------------------------
# 난이도 관련 설정
# -----------------------------
# HWP 문제 텍스트 안에서 난이도를 찾기 위한 정규식 패턴
# 대괄호 안팎, "난이도" 앞뒤의 공백 차이를 모두 허용합니다.
# 예: "[난이도] 상", "[난이도]상", "[ 난이도 ]  중" 등
DIFFICULTY_PATTERN = r"\[\s*난이도\s*\]\s*([상중하])"

# 허용되는 난이도 값 목록
VALID_DIFFICULTIES = ["상", "중", "하"]


# -----------------------------
# 중단원 관련 설정
# -----------------------------
# 허용되는 중단원 목록.
# - None으로 설정하면 중단원 유효성 검사를 건너뜁니다 (현재 기본값).
# - 목록을 지정하면 목록에 없는 중단원명은 예외 처리됩니다.
#
# 사용 예:
#   VALID_CHAPTERS = ["고차방정식", "연립방정식", "부등식", "이차함수"]
#
# 현재는 None으로 설정하여 모든 중단원을 허용합니다.
# 특정 중단원만 허용하려면 아래 목록에 추가하세요.
VALID_CHAPTERS = None  # None = 검사 안 함 / List[str] = 허용 목록


# -----------------------------
# 문제 번호 인식 패턴 (다양한 형식 지원)
# -----------------------------
# 문제 번호는 학교/문제은행마다 표기 형식이 다를 수 있으므로,
# 하나의 정규식으로 고정하지 않고 "여러 형식의 후보 목록"으로 관리합니다.
# core/problem_locator.py 에서 이 목록을 순서대로 시도하여,
# 문단의 시작 부분이 그중 하나라도 일치하면 '문제 시작 문단'으로 판단합니다.
#
# 새로운 번호 형식이 발견되면 이 리스트에 패턴만 추가하면 되므로 확장이 쉽습니다.
# 각 패턴은 반드시 문제 번호를 첫 번째 그룹 ( )으로 캡처해야 합니다.
PROBLEM_NUMBER_PATTERNS = [
    r"^\s*(\d{1,3})\.\s+",        # "1. ", "12. "
    r"^\s*(\d{1,3})\)\s+",        # "1) ", "12) "
    r"^\s*\((\d{1,3})\)\s+",      # "(1) "
    r"^\s*\[(\d{1,3})\]\s+",      # "[1] "
    r"^\s*(\d{1,3})\s*[.\)]\s*",  # 번호 뒤 공백 없이 바로 붙는 경우 등 (가장 느슨한 형태, 마지막에 시도)
]


# -----------------------------
# 문제 구분자 관련 설정
# -----------------------------
# 각 문제 사이를 구분하는 구분선 (txt 저장 시 사용, 참고용으로 유지)
PROBLEM_SEPARATOR = "-" * 62


# -----------------------------
# 파일명(출처) 관련 설정
# -----------------------------
# 파일명에서 학기 코드(1-1-a, 1-1-b 등)를 추출하기 위한 정규식
# 대괄호 안에 학년-학기-중간/기말 코드가 들어있는 형태를 가정
SEMESTER_CODE_PATTERN = r"\[(\d)-(\d)-([ab])\]"

# 학기 코드(a/b)를 실제 시험명으로 매핑
EXAM_TYPE_MAP = {
    "a": "중간",
    "b": "기말",
}

# 파일명에서 학교명을 추출하기 위한 정규식
# 예: [서울양천구][금옥여고][공수1] 형태에서 지역명 뒤, 과목명 앞의 대괄호 값을 학교명으로 간주
# 실제 파일명 규칙에 따라 core/filename_parser.py 에서 세부 로직으로 처리합니다.
SCHOOL_NAME_HINT_INDEX = 4  # 대괄호 그룹 중 학교명이 위치하는 순번(0부터 시작), 파서에서 조정 가능


# -----------------------------
# 기타
# -----------------------------
# GUI 로그창에 출력할 로그 최대 라인 수 (너무 길어지는 것 방지용, 필요시 조정)
MAX_LOG_LINES = 5000
